#!/bin/bash

# 📝 Описание:
# Этот скрипт демонстрирует различные примеры использования команды tree в Linux.
# Каждый пример сопровождается пояснением.
# После запуска скрипт покажет, как сделать его исполняемым.

echo "=== Примеры использования команды tree в Linux ==="
echo

# 1. Вывод структуры текущей директории
echo "1. Вывод структуры текущей директории:"
echo "tree"
echo

# 2. Вывод структуры указанной папки
echo "2. Вывод структуры указанной папки (например, /home/user/Documents):"
echo "tree /home/user/Documents"
echo

# 3. Вывод с глубиной 2 уровня
echo "3. Вывод структуры до глубины 2 уровня:"
echo "tree -L 2"
echo

# 4. Исключение определённых папок (например, node_modules и .git)
echo "4. Исключение папок node_modules и .git:"
echo "tree -I 'node_modules|.git'"
echo

# 5. Вывод скрытых файлов и папок
echo "5. Вывод скрытых файлов и папок:"
echo "tree -a"
echo

# 6. Запись результата в файл
echo "6. Запись результата в файл tree.md:"
echo "tree -L 2 > tree.md"
echo "Результат сохранён в tree.md"
echo

# 7. Вывод только папок (без файлов)
echo "7. Вывод только папок (без файлов):"
echo "tree -d"
echo

# 8. Вывод с полными путями к файлам
echo "8. Вывод с полными путями к файлам:"
echo "tree -f"
echo

# 9. Скрытие пустых папок
echo "9. Скрытие пустых папок:"
echo "tree --prune"
echo

# 10. Итоговое количество файлов и папок
echo "10. Итоговое количество файлов и папок:"
echo "tree --filelimit"
echo

echo "=== Инструкция по установке прав доступа для этого скрипта ==="
echo "Чтобы сделать этот скрипт исполняемым, выполните в терминале:"
echo "chmod +x tree.sh"
echo "После этого его можно запускать так:"
echo "./tree.sh"

tree ../.. --dirsfirst -L 4 -I 'node_modules|.git|docs' > tree.md

find . -name "*.md" -type f -exec du -h {} \;